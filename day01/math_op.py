#산술 연산자
print("10 + 4 =", 10 + 4)
print("10 - 4 =", 10 - 4)
print("10 * 4 =", 10 * 4)
print("10 / 4 =", 10 / 4)
print("10 // 4 =", 10 // 4)  #몫
print("10 % 4 =", 10 // 4)   #나머지
print("10 ** 4 =", 10 ** 4)  #10의 4제곱

# 도형의 면적
# 1. 한변의 길이가 5인 정사각형의 넓이
size = 5
area1 = size * size
print("정사각형의 넓이 :", area1)

# 2. 한변의 길이가 5이고, 높이가 7인 삼각형의 넓이
w = 7
h = 5
area2 = (w * h) / 2

print("삼각형의 넓이 : ", area2)